/*
 * port/rando/rando_newfile.c — upstream-parity new-file save state tables.
 *
 * The GBA randomizer initializes every new save through three byte tables
 * (newGame copy/OR/AND) compiled into its base patch. Natively we express
 * the two flag blobs as *named engine flags* (include/flags.h) instead of
 * raw bytes:
 *
 *  - the names are the engine's own identities from the decompilation, so
 *    each entry is independently verifiable against src/ (e.g.
 *    SOUGEN_06_HASHIGO is the North Hyrule Field ladder, LV1_*_* are
 *    Deepwood room flags);
 *  - flag NUMBERING is region-dependent (flags.h re-numbers several banks
 *    under EU), so a raw USA byte image would corrupt EU saves. Named
 *    enum constants compile to the right bit per region.
 *
 * kRandoNewFileBaselineFlags mirrors upstream's unconditional
 * `startingFlags` blob (116 bytes at gSave.flags): the "banish story"
 * baseline every rando file starts from. It complements (and overlaps)
 * ApplyStorySkip's gDemoSave-mirror; both are OR-applied to a zeroed file.
 * Missing pieces of this baseline were a softlock source: default `.logic`
 * reachability assumes them (e.g. HAKA_00_BW00, the Royal Valley bomb
 * wall, is open at baseline upstream — a no-bombs seed can otherwise
 * strand a graveyard placement).
 *
 * kRandoNewFileWorldOpenFlags mirrors upstream's `worldOpen` blob (308
 * bytes OR-ed over the same region) applied when the `openWorld`
 * eventdefine is set (World Settings "Open"): every permanently solvable
 * obstacle — cut trees, cracked blocks, bomb walls, boulder shortcuts,
 * non-key doors, bean vines, switches, levers, chest spawns, extendable
 * bridges — is pre-solved. Byte 0x23 of both blobs is the mCBF
 * eventdefine and stays with the m-poke applier (rando_runtime.c).
 *
 * Byte-level parity with the upstream blobs is asserted by
 * rando_logic_test (USA numbering; the test target defines no region
 * macro, which flags.h treats as USA).
 */

